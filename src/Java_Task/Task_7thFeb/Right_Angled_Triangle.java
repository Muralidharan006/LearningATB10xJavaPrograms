package Java_Task.Task_7thFeb;

public class Right_Angled_Triangle {
    public static void main(String[] args) {
        //*
        //* *
        //* * *
        //* * * *
        //* * * * *
        int[][] a = new int[5][5];
        for (int i = 0; i < a.length; i++) {

            for (int j = 0; j <= i; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }


    }
}
