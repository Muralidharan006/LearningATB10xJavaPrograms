package ex_03_Literals;

public class Lab018_Literals_P2 {
    public static void main(String[] args) {
        float pi = 3.14f;
        float pi2 = 3.14F; //Both F and f can use

        final int a = 10; //these is integral type of literal

        final int Age;
        Age = 18;
        //Age = 19;
        System.out.println(Age);

        float x = 10f; //possible with decimal
    }
}
